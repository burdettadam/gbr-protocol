/// `grimoire-export-owl` — serialise the Grimoire ontology as Turtle (RDF/OWL).
///
/// Usage:
///   grimoire-export-owl [--output <file>] [--format turtle|ntriples]
///
/// Reads the `core_primitives()` and `tag_key_mappings()` registries from
/// the ontology module and emits a lightweight OWL/Turtle file that can be
/// consumed by Protégé, `rdflib`, SPARQL endpoints, or any RDF toolchain.
///
/// The generated ontology is intentionally lightweight — it captures the
/// naming, layering, and projection relationships of each primitive without
/// attempting to express the full Grimoire data model in OWL DL.  Use the
/// generated JSON Schemas for structural validation; the OWL file is for
/// ontological documentation and cross-tool interoperability.
///
/// Example: check the triple count with
///   grimoire-export-owl | grep -c "^\s*grim:"

use std::fs;
use std::path::PathBuf;
#[allow(unused_imports)]
use std::fmt::Write; // required for writeln! et al. on String buffers

use grimoire_tooling::ontology::
    {core_primitives, tag_key_mappings, OntologyLayer, PrimitiveKind, ProjectionType};

// ── Namespace constants ───────────────────────────────────────────────────────

const GRIM: &str = "https://grimoire.systems/ontology#";
const RDF: &str = "http://www.w3.org/1999/02/22-rdf-syntax-ns#";
const RDFS: &str = "http://www.w3.org/2000/01/rdf-schema#";
const OWL: &str = "http://www.w3.org/2002/07/owl#";
const XSD: &str = "http://www.w3.org/2001/XMLSchema#";

fn main() {
    let args: Vec<String> = std::env::args().collect();
    let output_path = flag_value(&args, "--output").map(PathBuf::from);
    let _format = flag_value(&args, "--format").unwrap_or("turtle");

    let turtle = generate_turtle();

    match output_path {
        Some(path) => {
            fs::write(&path, &turtle).expect("could not write OWL output");
            eprintln!(
                "✓ Wrote Grimoire OWL ontology ({} bytes) to {}",
                turtle.len(),
                path.display()
            );
        }
        None => {
            print!("{turtle}");
        }
    }
}

// ── Turtle serialisation ──────────────────────────────────────────────────────

fn generate_turtle() -> String {
    let mut out = String::with_capacity(64 * 1024);

    // ── Ontology declaration ──────────────────────────────────────────────────
    writeln!(
        out,
        "# Grimoire Ontology — auto-generated by grimoire-export-owl\n\
         # Source: grimoire_tooling::ontology::core_primitives()\n\
         # Do not edit by hand — regenerate with: grimoire-export-owl --output grimoire.ttl\n"
    )
    .unwrap();

    writeln!(out, "@prefix grim:  <{GRIM}> .").unwrap();
    writeln!(out, "@prefix rdf:   <{RDF}> .").unwrap();
    writeln!(out, "@prefix rdfs:  <{RDFS}> .").unwrap();
    writeln!(out, "@prefix owl:   <{OWL}> .").unwrap();
    writeln!(out, "@prefix xsd:   <{XSD}> .\n").unwrap();

    writeln!(
        out,
        "<{GRIM}> a owl:Ontology ;\n    rdfs:label \"Grimoire Narrative Ontology\" ;\n    rdfs:comment \"Formal ontology for the Grimoire story-representation system.\" .\n"
    )
    .unwrap();

    // ── Meta-classes ──────────────────────────────────────────────────────────
    writeln!(out, "# ── Meta-classes ─────────────────────────────────────────\n").unwrap();
    for (local, label, comment) in &[
        ("OntologyPrimitive", "Ontology Primitive", "An atomic concept in the Grimoire ontology."),
        ("TagKey", "Tag Key", "A canonical annotation key used in <!-- key:value --> comments."),
        ("Projection", "Projection", "A mapping from an ontology primitive to a concrete representation."),
        ("Layer", "Ontology Layer", "The semantic stratum a primitive belongs to."),
    ] {
        writeln!(
            out,
            "grim:{local} a owl:Class ;\n    rdfs:label \"{label}\" ;\n    rdfs:comment \"{comment}\" .\n",
        )
        .unwrap();
    }

    // ── Primitives ────────────────────────────────────────────────────────────
    writeln!(out, "# ── Ontology Primitives ──────────────────────────────────\n").unwrap();

    for prim in core_primitives() {
        let local = turtle_local(&prim.id);
        let rdf_type = kind_to_owl_type(prim.kind);
        let layer_str = layer_to_str(prim.layer);

        writeln!(out, "grim:{local}").unwrap();
        writeln!(out, "    a {rdf_type}, grim:OntologyPrimitive ;").unwrap();
        writeln!(out, "    rdfs:label \"{}\" ;", escape_turtle(&prim.label)).unwrap();
        writeln!(out, "    grim:layer \"{}\" ;", layer_str).unwrap();
        writeln!(out, "    grim:kind \"{}\" ;", kind_to_str(prim.kind)).unwrap();
        writeln!(
            out,
            "    grim:canonicalSymbol \"{}\" ;",
            escape_turtle(&prim.canonical_symbol)
        )
        .unwrap();

        for inv in &prim.invariants {
            writeln!(out, "    grim:invariant \"{}\" ;", escape_turtle(inv)).unwrap();
        }

        for proj in &prim.projections {
            let proj_type = projection_type_to_str(proj.projection_type);
            write!(
                out,
                "    grim:projection [ a grim:Projection ; grim:projectionType \"{}\" ; grim:target \"{}\"",
                proj_type,
                escape_turtle(&proj.target)
            )
            .unwrap();
            if !proj.aliases.is_empty() {
                let aliases = proj
                    .aliases
                    .iter()
                    .map(|a| format!("\"{}\"", escape_turtle(a)))
                    .collect::<Vec<_>>()
                    .join(", ");
                write!(out, " ; grim:alias {aliases}").unwrap();
            }
            writeln!(out, " ] ;").unwrap();
        }

        // Replace last ` ;` with ` .` to close the Turtle subject block.
        if let Some(pos) = out.rfind(" ;") {
            if out[pos + 2..].trim().is_empty() {
                out.truncate(pos);
                out.push_str(" .\n");
            }
        }
        writeln!(out).unwrap();
    }

    // ── Tag key mappings ──────────────────────────────────────────────────────
    writeln!(out, "# ── Canonical Tag Keys ───────────────────────────────────\n").unwrap();

    for km in tag_key_mappings() {
        let local = format!("tag_{}", turtle_local(&km.canonical_key));
        write!(
            out,
            "grim:{local} a grim:TagKey ;\n    \
             rdfs:label \"{key}\" ;\n    \
             grim:canonicalKey \"{key}\" ;\n    \
             grim:layer \"{layer}\"",
            key = km.canonical_key,
            layer = layer_to_str(km.layer),
        )
        .unwrap();

        if let Some(alias) = &km.alias_key {
            write!(out, " ;\n    grim:alias \"{alias}\"").unwrap();
        }

        writeln!(out, " .\n").unwrap();
    }

    out
}

// ── Helpers ───────────────────────────────────────────────────────────────────

/// Convert a dotted primitive ID to a valid Turtle local name.
fn turtle_local(id: &str) -> String {
    id.replace('.', "_").replace('-', "_").replace('/', "_")
}

/// Map `PrimitiveKind` to the most appropriate OWL type IRI.
fn kind_to_owl_type(kind: PrimitiveKind) -> &'static str {
    match kind {
        PrimitiveKind::Entity         => "owl:Class",
        PrimitiveKind::Reference      => "owl:ObjectProperty",
        PrimitiveKind::AnnotationFact => "owl:AnnotationProperty",
        PrimitiveKind::VocabularyTerm => "owl:Class",
        PrimitiveKind::Constraint     => "owl:Class",
        PrimitiveKind::Projection     => "owl:Class",
    }
}

fn kind_to_str(kind: PrimitiveKind) -> &'static str {
    match kind {
        PrimitiveKind::Entity         => "entity",
        PrimitiveKind::Reference      => "reference",
        PrimitiveKind::AnnotationFact => "annotation_fact",
        PrimitiveKind::VocabularyTerm => "vocabulary_term",
        PrimitiveKind::Constraint     => "constraint",
        PrimitiveKind::Projection     => "projection",
    }
}

fn layer_to_str(layer: OntologyLayer) -> &'static str {
    match layer {
        OntologyLayer::Governance            => "governance",
        OntologyLayer::NarrativeDomain       => "narrative_domain",
        OntologyLayer::InterpretiveSemantics => "interpretive_semantics",
        OntologyLayer::AnnotationEvent       => "annotation_event",
        OntologyLayer::CatalogKnowledge      => "catalog_knowledge",
        OntologyLayer::StyleVoice            => "style_voice",
        OntologyLayer::GenerationTraining    => "generation_training",
        OntologyLayer::InterchangeSchema     => "interchange_schema",
    }
}

fn projection_type_to_str(pt: ProjectionType) -> &'static str {
    match pt {
        ProjectionType::RustType      => "rust_type",
        ProjectionType::MarkdownTag   => "markdown_tag",
        ProjectionType::YamlCatalog   => "yaml_catalog",
        ProjectionType::JsonSchema    => "json_schema",
        ProjectionType::PythonAdapter => "python_adapter",
        ProjectionType::CliOutput     => "cli_output",
    }
}

fn escape_turtle(s: &str) -> String {
    s.replace('\\', "\\\\")
        .replace('"', "\\\"")
        .replace('\n', "\\n")
        .replace('\r', "\\r")
}

fn flag_value<'a>(args: &'a [String], flag: &str) -> Option<&'a str> {
    args.iter()
        .position(|a| a == flag)
        .and_then(|p| args.get(p + 1))
        .map(|s| s.as_str())
}
